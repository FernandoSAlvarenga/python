# Crie uma função para cada operação matemática básica (soma, subtração, multiplicação e divisão) que recebe dois números como parâmetros
# e retorna o resultado da operação.
def soma(x, y):
    print(f"Soma: {x + y}")

def divisao(x, y):
    print(f"Divisão: {x / y}")

def multiplicacao(x, y):
    print(f"Multiplicação: {x * y}")

def subtracao(x, y):
    print(f"Subtração: {x - y}")

